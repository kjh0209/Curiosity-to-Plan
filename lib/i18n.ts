export type Language = "en" | "ko" | "ja" | "zh" | "es" | "fr" | "de";

const SUPPORTED_LANGUAGES: Language[] = ["en", "ko", "ja", "zh", "es", "fr", "de"];
const LANG_STORAGE_KEY = "skillloop_lang";

/**
 * Synchronously reads the user's language from localStorage, then falls back
 * to the browser language, then to "en". Safe to call during SSR (returns "en").
 * Use as a lazy useState initializer to avoid the English flash on first render.
 */
export function getInitialLanguage(): Language {
    if (typeof window === "undefined") return "en";
    const saved = localStorage.getItem(LANG_STORAGE_KEY);
    if (saved && SUPPORTED_LANGUAGES.includes(saved as Language)) return saved as Language;
    const browserLang = navigator.language.slice(0, 2).toLowerCase();
    if (SUPPORTED_LANGUAGES.includes(browserLang as Language)) return browserLang as Language;
    return "en";
}

/**
 * Persists the user's language preference to localStorage so the next page
 * load renders in the correct language immediately (no flash).
 */
export function saveLanguage(lang: Language): void {
    if (typeof window === "undefined") return;
    localStorage.setItem(LANG_STORAGE_KEY, lang);
}

